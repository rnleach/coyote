#include "test.h"

/*--------------------------------------------------------------------------------------------------------------------------
 *
 *                                                    Tests for Time
 *
 *-------------------------------------------------------------------------------------------------------------------------*/
static void
test_terminal_size(void)
{
    CoyTerminalSize ts = coy_get_terminal_size();
    printf("columns = %d rows = %d\n", ts.columns, ts.rows);
    Assert(ts.columns > 0 && ts.rows > 0);
    for(i32 i = 0; i < ts.columns; ++i)
    {
        putchar('=');
    }
    putchar('\n');
}

/*---------------------------------------------------------------------------------------------------------------------------
 *                                                     All time tests
 *-------------------------------------------------------------------------------------------------------------------------*/
void
coyote_terminal_tests(void)
{
    test_terminal_size();
}

